enum HeadsUpCategory { english, telugu, hindi, mix }

extension HeadsUpCategoryX on HeadsUpCategory {
  String get label => switch (this) {
        HeadsUpCategory.english => 'English Words',
        HeadsUpCategory.telugu => 'Telugu Movies',
        HeadsUpCategory.hindi => 'Hindi Movies',
        HeadsUpCategory.mix => 'All Mix',
      };

  String get emoji => switch (this) {
        HeadsUpCategory.english => '\u{1F4AC}',
        HeadsUpCategory.telugu => '\u{1F3AC}',
        HeadsUpCategory.hindi => '\u{1F3A5}',
        HeadsUpCategory.mix => '\u{1F3B2}',
      };

  List<String> words() => switch (this) {
        HeadsUpCategory.english => headsUpEnglish,
        HeadsUpCategory.telugu => headsUpTeluguMovies,
        HeadsUpCategory.hindi => headsUpHindiMovies,
        HeadsUpCategory.mix => [
            ...headsUpEnglish,
            ...headsUpTeluguMovies,
            ...headsUpHindiMovies,
          ],
      };
}
